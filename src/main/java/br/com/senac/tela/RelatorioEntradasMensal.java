    private void carregarTabelaEntradas() {
        DefaultTableModel defaultTable = (DefaultTableModel) tbContadorEntrada.getModel();
        defaultTable.setNumRows(0);
        contadorEntradas.stream().forEach(entra -> {
            defaultTable.addRow(new Object[]{
                entra.getData_cadastro(),
                entra.getHora(),
                entra.getQuantidade(),});
        });
    }

