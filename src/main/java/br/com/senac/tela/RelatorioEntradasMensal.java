    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
        LocalDate localDate = data.toInstant()
                .atZone(ZoneId.systemDefault())
                .toLocalDate();

       
        int mes = localDate.getMonthValue();
      
        data = varData.getDate();
        data1 = varDataFim.getDate();
        totalQuantidadeVisitantes = 0;

        if ((data == null)) {
            JOptionPane.showMessageDialog(null,
                    "Selecione uma data");
        } else {
            try {
                sessao = HibernateUtil.abrirConexao();

                contadorEntradas = contadorEntradaDao.pesquisarPorData(data, data1, sessao);
                for (ContadorEntrada contadorEntrada1 : contadorEntradas) {
                    totalQuantidadeVisitantes = totalQuantidadeVisitantes + contadorEntrada1.getQuantidade();
                }

                varTotal.setText(Integer.toString(totalQuantidadeVisitantes));

                if (contadorEntradas.isEmpty()) {
                    JOptionPane.showMessageDialog(null,
                            "Nenhum valor encontrado.");
                } else {
                    carregarTabelaEntradas();
                    // contadorEntradas = contadorEntradaDao.somar(tbContadorEntrada.getColumnCount(), data, sessao);

                }
            } catch (HibernateException e) {
                System.out.println("Erro ao pesquisar por data "
                        + e.getMessage());
            } finally {
                sessao.close();
            }
        }
    }//GEN-LAST:event_btPesquisarActionPerformed

    private int totalQuantidadeVisitantes(ContadorEntrada contadorEntrada) {

        return totalQuantidadeVisitantes + contadorEntrada.getQuantidade();

    }
